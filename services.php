        <h1>Alliance Impairment Management Services</h1>
            <p>Alliance Impairment Management prides itself in cost-effective services, with expert staff in various locations to handle the increasingly complex nature of medical case management.</p>
            <p>Our service commitment to our customers includes expert case management assessment and planning in a timely and cost effective manner with case resolution as the primary goal, and we strive to successfully reach optimal outcomes for each individual case.</p>

            <h2>Medical Case Management</h2>
            <p>AIM, Inc. is based on a team work concept in providing optimal claim resolution by providing expert medical case management services to the injured worker, the Claim Representative and to Employers. A case can be referred for full field-based case management or as a task assignment which might include a review of the medical or a one time attendance at a medical appointment. Our Case Managers will address diagnosis, causation of the work incident to the diagnosis, underlying medical or pre-existing trauma conditions, return to work potential and target to modified duty and full duty and recovery time-frames with the Medical Providers.</p>
            <p>Communication of the assessment information will discussed, faxed or emailed to the Claim Professional and Employer following the assessment process. Critical events, complications, or issues will be discussed immediately with all parties to assist with claim review.</p>

            <h2>Life Care Planning</h2>
            <p>A Referral to complete a Life Care Plan involves the following services: Upon receipt of a case, an intake will be completed to establish referral information and a billing agreement with the referral source; medical records are reviewed; supporting documentation is gathered; Attorney contacts are made; and arrangements are made for the initial interview with the client. The appropriate therapeutic team will be consulted and preliminary Life Care Plan options prepared. Costs and resources will be documented and projections finalized into the Plan. The Plan will then be distributed to the appropriate parties. AIM currently employs three Certified Life Care Planners who successfully completed 132 hours of study, completed a practice Life Care Plan and passed the test for board certification. We have a list of successful plan references available on demand.</p>
            <p>A Referral for a Critique of an existing Life Care Plan involves the following: Upon receipt of the Plan, billing information will be established. A report will be generated to list areas of concern on the treatment plan, equipment, medication, complications anticipated, home modification, cost or sources of information. Provide Medical Case Management services for catastrophic cases of which a Life Care Plan has already been developed. We work with Trust Administrators to insure that appropriate care is rendered to the injured individual for which the Plan was developed. A report will be generated to document the assessment of compliance with the Plan, make short and long term recommendations and give an updated overview of the injured person’s present medical condition.</p>

            <h2>Medicare Set-Aside Allocation</h2>
            <p>This is a system initiated by Centers for Medicare and Medicaid Services (CMS) to assure that medical costs associated with a work-related injury are not paid through Medicare. A Workers’ Compensation settlement must include an MSA when: Class 1 – The individual is a Medicare Beneficiary at the time of settlement regardless of the amount of settlement. Class II – The individual is NOT a Medicare Beneficiary at the time of settlement but meets these two (2) requirements: A) The total amount of settlement is over $250,000.00 B) There is a reasonable expectation of Medicare enrollment within thirty (30) months of the settlement date. The role of the Medicare Set-Aside Allocator is identifying projected medical costs through interview of treating physicians, review of medical records and research for standards of care and pricing information, to assure that adequate funding is allocated for Medicare reimbursable services. The cost of any recommended treatment, equipment, or service that is not covered by Medicare is not included in the Set-Aside, but documented separately. The figures must be approved by CMS prior to the settlement and are submitted to CMS by the attorney along with supporting documentation. A certification has been developed recently for Medicare Set-Aside Allocator. After completing a 32 hour course of study, the candidate is eligible to take the certification examination. AIM currently employs a board eligible Medicare Set-Aside Allocator who is also a Certified Life Care Planner.</p>

            <h2>IME Coordination</h2>
            <p>Upon receipt of this type of referral, medical records will be reviewed and organized and the Claim Adjuster and/or Defense Attorney contacted. AIM will attempt to ensure that comprehensive are obtained. The appropriate physician specialty will be contacted and a date and time scheduled. The injured/ill person will be notified by certified and first class mail of the details of the IME. A letter will be composed requesting that the IME Physician address specific issues in his evaluation and in his narrative report to accompany the medical records. The Employer will be notified of the scheduled exam date. Forms such as Physical Capacity form, Affidavit of Recovery, or Hand Function form will be enclosed. Only with approval of Claims, will films be picked up and hand carried to the IME and/or transportation arranged for the injured/ill person. The IME will be attended and immediate feedback reported to Claims. The IME narrative report will be received and reviewed to insure that all areas of concern have been addressed, if not, the IME physician will be contacted and an addendum requested. A report will be generated when all activity is completed.</p>

            <h2>File Review</h2>
            <p>File Review of Open Claims – An on-site review or telephone conference of current claims with the claims’ management team and legal council can be requested from AIM. The CM assigned will assist Claims in making decisions on acceptance, denial or management from a medical viewpoint to set reserves, give appropriate direction to the injured/ill person, assess current treatment plans and time frames, make suggestion on future medical referrals and discuss strategies.</p>

            <h2>Cost Projection / Medical Bill Audits</h2>
            <p>Cost Projection – AIM will compile a list of current and anticipated costs from treatment team to assist Claims in setting reserves. This can be limited to medical, vocational or home modification, or give analysis of all areas of interest. Also an Audit of medical bills for what would be considered relatedness or medical necessity to the injury/illness and also report would address usual and customary billing practices.</p>
